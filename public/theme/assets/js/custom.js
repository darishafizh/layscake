function info(msg) {
    iziToast.info({
        title: "Info",
        message: msg,
        position: "topRight",
    });
}

function success(msg) {
    iziToast.success({
        title: "Success",
        message: msg,
        position: "topRight",
    });
}

function warning(msg) {
    iziToast.warning({
        title: "Warning",
        message: msg,
        position: "topRight",
    });
}

function error(msg) {
    iziToast.error({
        title: "Error",
        message: msg,
        position: "topRight",
    });
}
